    private void btnIngresarMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarMateriaActionPerformed
        String nombre = txtMateria.getText();
        String descripcion = txtDescripcionTarea.getText();
        String fechaEnvio = txtxFechaEnvio.getText();
        String fechaEntrega = txtFechaEntrega.getText();
        String horaEntrega = txtHora.getText();
        Tarea tareaNew = new Tarea(nombre, descripcion, fechaEnvio, fechaEntrega, horaEntrega);
        c1.agregarFinal(tareaNew);
        this.lista.actualizarLista();
    }//GEN-LAST:event_btnIngresarMateriaActionPerformed

